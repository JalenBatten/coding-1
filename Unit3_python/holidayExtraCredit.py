def holidaySchedule(date):
    if date == '12/21/24':
        print('Play video games.')
    elif date == '12/24/24':
        print('Have a christmas party.')
    elif date == '12/25/24':
        print('Celebrate Christmas')
    elif date == '12/26/24':
        print('Visit family members.')
    elif date == '12/31/24':
        print('Celebrate New Years')
    elif date == '1/6/24':
        print('Get prepared for school.')
    else:
        print('Nothing is planned for this day.')

holidaySchedule('12/21/24')
holidaySchedule('12/25/24')
holidaySchedule('12/26/24')
